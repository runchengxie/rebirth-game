//
// 数据来源：理杏仁「沪深300 月度涨跌幅」历史列表（价格指数口径），
// 与中证指数月度报告交叉核对。年度合计：
//   2023 全年 -11.38%，2024 全年 +14.68%，2025 全年 +17.66%。
//
// 设计说明：真实行情在这里只扮演两种角色——
//   1. 「天气」：推荐跟踪净值按 决策敞口 × 当月真实涨跌 + 决策自带 alpha 滚动；
//   2. 「剧情素材」：结算文案区分「过程对/结果对」四个象限。
// 它不参与研究过程评分（证据/清晰/风控/协作/生活），因为短期价格是噪声，
// 分析做对了市场也可能不给面子——这正是游戏想让玩家体验的张力。

export const HS300_MONTHLY_RETURNS: Record<string, number[]> = {
  "2023": [0.0737, -0.021, -0.0046, -0.0054, -0.0572, 0.0116, 0.0448, -0.0621, -0.0201, -0.0317, -0.0214, -0.0186],
  "2024": [-0.0629, 0.0935, 0.0061, 0.0189, -0.0068, -0.033, -0.0057, -0.0351, 0.2097, -0.0316, 0.0066, 0.0047],
  "2025": [-0.0299, 0.0191, -0.0007, -0.03, 0.0185, 0.025, 0.0354, 0.1033, 0.032, 0.0, -0.0246, 0.0228],
};

// 未收录的年份（如 demo 示范章节）返回 0：净值退化为纯 alpha 驱动，行为与
// 旧版一致，剧情结算文案也不会出现真实行情段落。
export function marketReturnFor(year: string, monthIndex: number): number {
  const months = HS300_MONTHLY_RETURNS[year];
  if (!months || monthIndex < 0 || monthIndex >= months.length) return 0;
  return months[monthIndex];
}
